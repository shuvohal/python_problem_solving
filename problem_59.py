#Nested List Element Access: Given a nested list, write a Python program to access and print specific elements from it.
nested_list = [
    [10, 20, 30],
    [40, 50, 60],
    [70, 80, 90]
]

# Access specific elements
print("Element at [0][1]:", nested_list[0][1])   # 20
print("Element at [1][2]:", nested_list[1][2])   # 60
print("Element at [2][0]:", nested_list[2][0])   # 70
